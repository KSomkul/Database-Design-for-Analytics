-- Q.1: Find the list of ingredients and the name of vendor that offer the best price
select l.min_price, v.name vendor_name, ing.name ing_name  
from (
    Select min(c.unitprice) min_price, i.ingredient_id
        from vendor v, ingredient i, ingred_vendor c
        where c.vendor_vendor_id=v.vendor_id and
              c.ingredient_ingredient_id=i.ingredient_id
        group by i.ingredient_id) l, 
    ingred_vendor iv, 
    vendor v, 
    ingredient ing
        where l.ingredient_id=iv.ingredient_ingredient_id and
              v.vendor_id=iv.vendor_vendor_id and
              ing.ingredient_id=l.ingredient_id and
              l.min_price=iv.unitprice
  
